package udaraAsia;

public class Aeroplane {
	
	private String aeroId;
	private int busiCapacity;
	private int ecoCapacitiy;
	private boolean working;
	
	
	public String getAeroId() {
		return aeroId;
	}
	public void setAeroId(String aeroId) {
		this.aeroId = aeroId;
	}
	public int getBusiCapacity() {
		return busiCapacity;
	}
	public void setBusiCapacity(int busiCapacity) {
		this.busiCapacity = busiCapacity;
	}
	public int getEcoCapacitiy() {
		return ecoCapacitiy;
	}
	public void setEcoCapacitiy(int ecoCapacity) {
		this.ecoCapacitiy = ecoCapacity;
	}
	public boolean getWorking() {
		return working;
	}
	public void setWorking(boolean working) {
		this.working = working;
	}
	
	
}
